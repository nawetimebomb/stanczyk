#include "stanczyk.h"
#include "tasker.h"

#include "assembly.h"
#include "backend.h"
#include "codegen.h"
#include "memory.h"
#include "output.h"
#include "fileman.h"
#include "timer.h"
#include "frontend.h"
#include "ir_code.h"
#include "typecheck.h"

void run_tasker(void) {
    start_filemanager();
    start_timer();

    TIMER(FRONTEND);
    IRCodeChunk *chunk = start_ir_code_chunk();
    frontend_run(chunk);

    TIMER(TYPECHECK);
    typecheck_run(chunk);

    TIMER(CODEGEN);
    Assembly *assembly = start_assembly();
    codegen_run(chunk, assembly);

    stop_ir_code_chunk(chunk);

    TIMER(OUTPUT);
    output_run(assembly);

    stop_assembly(assembly);

    TIMER(BACKEND);
    backend_run();

    stop_timer();
    stop_filemanager();
}
