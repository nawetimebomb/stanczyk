#include <stdlib.h>
#include <string.h>
#include <time.h>

#include "stanczyk.h"

#include "timer.h"
#include "logger.h"
#include "memory.h"

typedef struct {
    bool started;
    StanczykSteps step;
    double value;
    double total_time;
} Timer;

Timer *timer;

void start_timer(void) {
    timer = ALLOCATE(Timer);
}

void stop_timer(void) {
    TIMER(TOTAL);
    CLI_MESSAGE(PREFIX_STANCZYK, "Compilation process complete in %fs\n", timer->total_time);
    free(timer);
}

void TIMER(StanczykSteps step) {
    double now = (double)clock() / CLOCKS_PER_SEC;

    if (step == TOTAL) {
        double result = now - timer->value;
        timer->started = false;
        CLI_MESSAGE(PREFIX_NONE, "→ done in %fs\n", result);
        timer->total_time += result;
        return;
    }

    if (timer->started) {
        double result = now - timer->value;
        timer->started = false;
        CLI_MESSAGE(PREFIX_NONE, "→ done in %fs\n", result);
        timer->total_time += result;
        TIMER(step);
    } else {
        timer->started = true;
        CLI_MESSAGE(PREFIX_STANCZYK, "%-25s ", CLI_GET_STEP_NAME(step));
        timer->value = now;
        timer->step = step;
    }
}
