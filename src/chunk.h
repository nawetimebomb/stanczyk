#ifndef STANCZYK_CHUNK_H
#define STANCZYK_CHUNK_H

#include "common.h"
#include "constant.h"

typedef enum {
    // Constants
    OP_PUSH_INT,
    OP_PUSH_STR,
    // Keywords
    OP_JUMP,
    OP_JUMP_IF_FALSE,
    OP_LOOP,
    OP_MEMORY,
    // Intrinsics
    OP_ADD,
    OP_AND,
    OP_DEC,
    OP_DIVIDE,
    OP_DROP,
    OP_DUP,
    OP_EQUAL,
    OP_GREATER,
    OP_GREATER_EQUAL,

    OP_INC,
    OP_LESS,
    OP_LESS_EQUAL,
    OP_LOAD8,

    OP_MULTIPLY,
    OP_NOT_EQUAL,
    OP_OR,

    OP_OVER,
    OP_PRINT,
    OP_RETURN,
    OP_SAVE8,
    OP_SUBSTRACT,
    OP_SWAP,
    OP_SYS4,
    // Special
    OP_END
} Op;

typedef struct {
    int start;
    int count;
    int capacity;
    bool erred;
    u8 *code;
    int *lines;
    ConstantArray constants;
} Chunk;

void init_chunk(Chunk *);
int add_constant(Chunk *, Value);
void write_chunk(Chunk *, u8, int);
void free_chunk(Chunk *);

#endif
