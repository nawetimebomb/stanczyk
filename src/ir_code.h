#ifndef STANCZYK_IR_CODE_H
#define STANCZYK_IR_CODE_H

#include "constant.h"
#include "scanner.h"

typedef enum {
    // Constants
    OP_PUSH_INT,
    OP_PUSH_STR,

    // Intrinsics
    OP_ADD,
    OP_DIVIDE,
    OP_DROP,
    OP_MODULO,
    OP_MULTIPLY,
    OP_PRINT,
    OP_SUBSTRACT,
    OP_EQUAL,
    OP_NOT_EQUAL,
    OP_LESS,
    OP_LESS_EQUAL,
    OP_GREATER,
    OP_GREATER_EQUAL,

    // Syscalls
    // TODO: Replace with functions that pop to specific
    // registers and then do the call
    OP_SYSCALL0,
    OP_SYSCALL1,
    OP_SYSCALL2,
    OP_SYSCALL3,
    OP_SYSCALL4,
    OP_SYSCALL5,
    OP_SYSCALL6,

    // Special
    OP_EOC
} OpCode;

typedef struct {
    OpCode type;
    Token  token;
    Value  operand;
} Code;

typedef struct {
    int start;
    int count;
    int capacity;
    Code *code;
    int *lines;
} IRCodeChunk;

IRCodeChunk *start_ir_code_chunk(void);
void stop_ir_code_chunk(IRCodeChunk *chunk);
void write_ir_code_chunk(IRCodeChunk *chunk, Code code);

#endif
